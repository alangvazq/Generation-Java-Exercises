
public class Calculadora {
	public int suma(int num1, int num2) {
		return num1+num2;
	}//suma
	
	public int multiplica(int num1, int num2) {
		return num1*num2;
	}//multiplica
	
	public int divide(int num1, int num2) {
		return num1/num2;
	}//divide
	
	public float getPI() {
		return 3.1415926f;
	}//get 3.1415926f
	
	public String getStrPI() {
		return String.valueOf(getPI());
	}
}//class Calculadora
